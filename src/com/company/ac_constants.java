/* Constants */
package com.company;
public class ac_constants {
    public static void main(String[] args) {
/*
let's declare a variable called pi.And set it
to 3.14, now here we need to add an F to
represent this as a float,you know that we use
pi to calculate the area of a circle. What if
before we calculate the area of a circle, I
come here and type pi equals 1, then all our
calculations are gonna get messed, up, we don't
want this to happen. That's when we use constants.
So, if we type final here, Java compiler will treat
this as a constant, so once we initialize it, we
can't change this.
*/
        final float pi = 3.14f;
        //pi=1; will throw error
    }
}
